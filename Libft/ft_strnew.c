#include "libft.h"

char		*ft_strnew(size_t size)
{
	char	*s;

	if (!(s = (char *)malloc(sizeof(char) * (size + 1))))
		return (NULL);
	if (size + 1 <= size)
		return (NULL);
	ft_bzero(s, size + 1);
	return (s);
}
