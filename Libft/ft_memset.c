#include "libft.h"

void		*ft_memset(void *b, int c, size_t len)
{
	unsigned char	*str;

	if (len == 0)
		return (b);
	str = (unsigned char *)b;
	while (len--)
	{
		*str = (unsigned char)c;
		if (len)
			str++;
	}
	return (b);
}
